<template>
  <!-- Search Bar -->
  <div class="flex justify-center">
    <div class="w-full p-10 sm:w-3/5">
      <div class="flex justify-between bg-white rounded-lg shadow-xl">
        <div class="p-3">
          <div class="flex items-center justify-center w-12 h-12 text-gray-400 rounded-full focus:outline-none">
            <BaseIcon view-box="0 0 20 20" fill="currentColor">
              <path fill-rule="evenodd" d="M8 4a4 4 0 100 8 4 4 0 000-8zM2 8a6 6 0 1110.89 3.476l4.817 4.817a1 1 0 01-1.414 1.414l-4.816-4.816A6 6 0 012 8z" clip-rule="evenodd" />
            </BaseIcon>
          </div>
        </div>
        <input
          id="search"
          v-model="query"
          class="flex justify-start w-full px-4 py-4 leading-tight text-gray-700 rounded-l-full sm:w-full focus:outline-none "
          type="text"
          aria-label="search"
          placeholder="Search"
        >
      </div>
    </div>
  </div>

  <section v-if="errored" class="flex items-center justify-center p-4">
    <p>We are sorry. We are unable to retrieve this information at this time. Please retry later.</p>
  </section>

  <section v-else>
    <div class="flex items-center justify-center">
      <div v-if="loading">
        {{ waiting }}
      </div>
      <div v-else class="flex flex-wrap justify-center">
        <div v-for="product in info" :key="product.description" class="w-auto px-1 m-5 my-1 lg:w-1/3 lg:w-auto md:w-auto lg:my-4 lg:px-4">
          <div class="overflow-hidden rounded-lg shadow-lg">
            <img
              alt="Placeholder"
              class="block object-cover"
              :src="product.small"
            >
          </div>
        </div>
      </div>
    </div>
  </section>
</template>

<script lang="ts" setup>
import { defineComponent, ref, watch } from 'vue'
import axios from 'axios'
import _ from 'lodash'

const info = ref(null)
const loading = ref(true)
const errored = ref(false)
const query = ref('')
const debouncedGetQuery = _.debounce(searchProducts, 500)
const waiting = ref('Type query to search products!')

function searchProducts() {
  if (query.value === '') {
    info.value = null
    loading.value = true
    waiting.value = 'Type query to search products!'
    return
  }
  axios
    .get(`/.netlify/functions/search-api?&${new URLSearchParams({
      query: query.value,
    })}`)
    .then((response) => {
      info.value = response.data
      errored.value = false
    })
    .catch((error) => {
      console.log(error)
      errored.value = true
    })
    .finally(() => loading.value = false)
}

watch(query, (newQuery, oldQuery) => {
  loading.value = true
  waiting.value = 'Waiting...'
  debouncedGetQuery()
})
</script>
